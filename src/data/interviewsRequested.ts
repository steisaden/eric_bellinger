type RequestedInterviewsSource = typeof REQUESTED_INTERVIEWS_SOURCE;

type RequestedInterviewSourceItem = RequestedInterviewsSource["items"][number];

import type { Interview } from "@/types";

export type RequestedInterview = Interview & {
  type: string;
  hostOrChannel: string;
  verificationStatus: string;
  verificationLabel: string;
};

const TYPE_LABELS: Record<string, string> = {
  video_interview: "Video Interview",
  video_interview_article: "Video Interview",
  written_interview: "Written Q&A",
  written_interview_article: "Written Q&A",
  podcast_episode: "Podcast Episode",
  podcast_episode_video: "Podcast Episode",
  red_carpet_interview: "Red-Carpet Interview",
  podcast_or_webshow_episode: "Podcast / Webshow",
};

const VERIFICATION_LABELS: Record<string, string> = {
  verified_public_source: "Verified public source",
  verified_listing_needs_direct_episode_url: "Listing needs direct episode URL",
};

const SOURCE_HOST_OVERRIDES: Array<[RegExp, string]> = [
  [/youtube\.com$/i, "YouTube"],
  [/youtu\.be$/i, "YouTube"],
  [/vladtv\.com$/i, "VladTV"],
  [/power106\.com$/i, "Power 106"],
  [/iheart\.com$/i, "iHeart"],
  [/swaysuniverse\.com$/i, "Sway's Universe"],
  [/hip-hopvibe\.com$/i, "Hip-Hop Vibe"],
  [/youknowigotsoul\.com$/i, "YouKnowIGotSoul"],
  [/complex\.com$/i, "Complex"],
  [/apple\.com$/i, "Apple"],
];

function getHostname(url: string) {
  try {
    const hostname = new URL(url).hostname.replace(/^www\./i, "");
    const override = SOURCE_HOST_OVERRIDES.find(([pattern]) => pattern.test(hostname));
    return override ? override[1] : hostname;
  } catch {
    return "Source";
  }
}

function getYouTubeVideoId(url: string) {
  try {
    const parsed = new URL(url);
    const hostname = parsed.hostname.replace(/^www\./i, "");
    if (hostname === "youtu.be") {
      return parsed.pathname.replace(/^\//, "") || null;
    }

    if (hostname.endsWith("youtube.com")) {
      if (parsed.pathname === "/watch") {
        return parsed.searchParams.get("v");
      }

      const shortPath = parsed.pathname.match(/^\/(?:embed|shorts)\/([^/?#]+)/i);
      if (shortPath) {
        return shortPath[1] ?? null;
      }
    }
  } catch {
    return null;
  }

  return null;
}

export const REQUESTED_INTERVIEWS_META = REQUESTED_INTERVIEWS_SOURCE.meta;

export const REQUESTED_INTERVIEWS: RequestedInterview[] = REQUESTED_INTERVIEWS_SOURCE.items.map((item: RequestedInterviewSourceItem) => {
  const videoId = getYouTubeVideoId(item.primary_url);

  return {
    id: item.id,
    title: item.title,
    type: item.type,
    typeLabel: TYPE_LABELS[item.type] ?? item.type.replace(/_/g, " "),
    showOrPublisher: item.show_or_publisher,
    hostOrChannel: item.host_or_channel,
    publishedDate: item.published_date,
    year: item.published_date ? Number(item.published_date.slice(0, 4)) : null,
    sourceUrl: item.primary_url,
    alternateUrls: item.alternate_urls ?? [],
    topics: item.topics ?? [],
    verificationStatus: item.verification_status,
    verificationLabel: VERIFICATION_LABELS[item.verification_status] ?? item.verification_status.replace(/_/g, " "),
    sourceHost: getHostname(item.primary_url),
    isPlayable: Boolean(videoId),
    videoId,
    embedId: videoId,
    thumbnailUrl: videoId ? `https://img.youtube.com/vi/${videoId}/hqdefault.jpg` : null,
  };
});
